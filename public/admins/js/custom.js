"use strict";

var title = document.getElementsByTagName("title")[0].innerHTML;
(function titleScroller(text) {
    document.title = text;
    setTimeout(function () {
        titleScroller(text.substr(1) + text.substr(0, 1));
    }, 500);
}(title + " ~ "));